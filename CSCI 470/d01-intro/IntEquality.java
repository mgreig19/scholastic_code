// Fig. 7.22b: IntEquality
// Integer object equality

public class IntEquality
{
   public static void main(String[] args)
   {
       Integer ibox = 3;
       Integer jbox = 3;

       if (ibox == jbox)
	   System.out.printf ("ibox == jbox");
       else
	   System.out.printf("ibox != jbox");

   } 
}
